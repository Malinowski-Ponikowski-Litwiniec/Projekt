package com.google.firebase.quickstart.auth;

public class curMacro {
    double amount;
    double carbs;
    double fat;
    double kcal;
    double protein;

    public curMacro(double amount, double carbs, double fat, double kcal, double protein) {
        this.amount = amount;
        this.carbs = carbs;
        this.fat = fat;
        this.kcal = kcal;
        this.protein = protein;
    }

}
